def run_script(script_path, description):
    """‡∏£‡∏±‡∏ô‡∏™‡∏Ñ‡∏£‡∏¥‡∏õ‡∏ï‡πå‡πÅ‡∏•‡∏∞‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå"""
    logging.info(f"üöÄ ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡∏£‡∏±‡∏ô {description}...")
    
    try:
        result = subprocess.run(
            [sys.executable, script_path],
            capture_output=True,
            text=True,
            check=True
        )
        
        # ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å stdout ‡∏•‡∏á‡πÉ‡∏ô‡πÑ‡∏ü‡∏•‡πå log
        log_filename = f"{os.path.splitext(script_path)[0]}_output.log"
        with open(log_filename, "w", encoding="utf-8") as f:
            f.write(result.stdout)
        
        logging.info(f"‚úÖ ‡∏£‡∏±‡∏ô {description} ‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à")
        logging.info(f"üìù ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡πÑ‡∏ß‡πâ‡∏ó‡∏µ‡πà: {log_filename}")
        return True
    
    except subprocess.CalledProcessError as e:
        logging.error(f"‚ùå ‡πÄ‡∏Å‡∏¥‡∏î‡∏Ç‡πâ‡∏≠‡∏ú‡∏¥‡∏î‡∏û‡∏•‡∏≤‡∏î‡πÉ‡∏ô‡∏Å‡∏≤‡∏£‡∏£‡∏±‡∏ô {description}")
        logging.error(f"stderr: {e.stderr}")
        return False

